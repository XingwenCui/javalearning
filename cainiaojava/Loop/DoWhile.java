package cainiaojava.Loop;

public class DoWhile {
    public static void main(String[] args){
        int x = 10;
        // When x = 20, loop will stop
        do{
           System.out.print("value of x : " + x );
           x++;
           System.out.print("\n");
        }while( x < 20 );
     }
    
}
