package cainiaojava.Character;

public class CharMethods {
    public static void main(String[] args) {
        char letter = 'a';
        char digit = '1';
        char characters = '!';

        System.out.println(Character.isLetter(letter));
        System.out.println(Character.isDigit(digit));
        System.out.println(Character.isLetter(characters));
    }
}
